    public function LoginBloger(Request $request)
    {
        $request->validate([
            'email' => 'required',
            'password' => 'required',
        ]);

        $credentials = $request->only('email', 'password');
        if (Auth::attempt($credentials)) {
            return redirect()->intended('/bloger')
                            ->withSuccess('Berhasil Login!');
        }

        return redirect("login")->withSuccess('Login gagal !');
    }

    public function dashboard()
    {
        if(Auth::check()) {
            return view('admin.index');
        }

        return redirect('/login')->withSuccess('Tidak boleh mengakses ini ya!');
    }

    public function signOut()
    {
        Session::flush();
        Auth::logout();

        return Redirect('/login');
    }
